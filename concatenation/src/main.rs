fn main() {
    // Using the + operator to combine two String values into a new String value.
    let s1 = String::from("Hello, ");
    let s2 = String::from("world!");
    let s3 = s1 + &s2; // Note that s1 has been moved here and can no longer be used.
    println!("{}", s3);

    // If we need to concatenate multiple strings, the behavior of the + operator gets unwieldy.
    let s1 = String::from("tic");
    let s2 = String::from("tac");
    let s3 = String::from("toe");
    let s = s1 + "-" + &s2 + "-" + &s3;
    println!("{}", s);

    // For combining strings in more complicated ways, we can instead use the format! macro.
    // The version of the code using format! is much easier to read, and the code generated by the
    // format! macro uses references so that this call doesn't take ownership of any of its parameters.
    let s1 = String::from("tic");
    let s2 = String::from("tac");
    let s3 = String::from("toe");
    let s = format!("{s1}-{s2}-{s3}");
    println!("{}", s);
}